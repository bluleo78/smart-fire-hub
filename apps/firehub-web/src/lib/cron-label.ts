  '0 9 * * *': '매일 오전 9시',
  '0 8 * * *': '매일 오전 8시',
  '0 7 * * *': '매일 오전 7시',
  '0 6 * * *': '매일 오전 6시',
  '0 18 * * *': '매일 오후 6시',
  '0 9 * * 1': '매주 월요일 오전 9시',
  '0 9 * * 5': '매주 금요일 오전 9시',
  '0 9 * * 1-5': '평일 오전 9시',
  '0 * * * *': '매시간',
